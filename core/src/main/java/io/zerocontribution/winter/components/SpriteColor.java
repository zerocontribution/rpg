package io.zerocontribution.winter.components;

import com.artemis.Component;

public class SpriteColor extends Component {

    public float r = 1;
    public float g = 1;
    public float b = 1;
    public float a = 1;

    public SpriteColor(float r, float g, float b, float a) {
        this.r = r;
        this.g = g;
        this.b = b;
        this.a = a;
    }

}
